package application;

public class Coordinate {
	private int row;
	private int col;
	private char letter;
	
	public int getRow() {
        return this.row;
    }
 
    public void setRow(int row) {
        this.row = row;
    }
    
    public int getCol() {
        return this.col;
    }
 
    public void setCol(int col) {
        this.col = col;
    }
    
    public char getLetter() {
        return this.letter;
    }
 
    public void setLetter(char letter) {
        this.letter = letter;
    }

}